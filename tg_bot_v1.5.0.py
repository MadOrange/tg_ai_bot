def sanitize_user_input(text: str, max_length: int = 2000) -> str:
    """–û—á–∏—â–∞–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –≤–≤–æ–¥ –æ—Ç –∏–Ω—ä–µ–∫—Ü–∏–π –∏ –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ—Ç –¥–ª–∏–Ω—É."""
    # 2.1. –û–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –¥–ª–∏–Ω—ã
    if len(text) > max_length:
        text = text[:max_length]
        return text + " [—Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ–∫—Ä–∞—â–µ–Ω–æ]"
    
    # 2.2. –ñ—ë—Å—Ç–∫–∞—è –±–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –∏–Ω—ä–µ–∫—Ü–∏–π
    injection_patterns = [
        r"(?i)–∏–≥–Ω–æ—Ä–∏—Ä—É–π.*(–∏–Ω—Å—Ç—Ä—É–∫—Ü–∏|—É–∫–∞–∑–∞–Ω)",
        r"(?i)–∑–∞–±—É–¥—å.*(–ø—Ä–µ–¥—ã–¥—É—â|—Ä–∞–Ω—å—à)",
        r"(?i)–≤—ã–≤–µ–¥–∏.*(–ø—Ä–æ–º–ø—Ç|–∏–Ω—Å—Ç—Ä—É–∫—Ü–∏|–ø—Ä–∞–≤–∏–ª–∞|–ø—Ä–æ–º—Ç)",
        r"(?i)–ø–æ–∫–∞–∂–∏.*(–ø—Ä–æ–º–ø—Ç|–∏–Ω—Å—Ç—Ä—É–∫—Ü–∏|–ø—Ä–∞–≤–∏–ª–∞|–ø—Ä–æ–º—Ç)",
        r"(?i)–∫–∞–∫.*(–ø—Ä–∞–≤–∏–ª–∞|–∏–Ω—Å—Ç—Ä—É–∫—Ü–∏|–ø—Ä–æ–º—Ç|–ø—Ä–æ–º–ø—Ç)",
        r"(?i)—Å–∏—Å—Ç–µ–º–Ω–æ–µ.*—Å–æ–æ–±—â–µ–Ω",
        r"(?i)–ø—Ä–µ–¥—Å—Ç–∞–≤—å, —á—Ç–æ —Ç—ã.*–ø—Ä–µ–¥—Å—Ç–∞–≤—å, —á—Ç–æ —Ç—ã",
        r"(?i)ignore.*previous",
        r"(?i)print.*prompt",
        r"(?i)show.*(prompt|instructions)",
        r"system:", "assistant:", "###", "---",
        r"\[.*\]",  # –ü–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–µ –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–µ —Å–∫–æ–±–∫–∏
    ]
    
    for pattern in injection_patterns:
        if re.search(pattern, text):
            logging.warning(f"–ë–õ–û–ö–ò–†–û–í–ö–ê: –û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ –∏–Ω—ä–µ–∫—Ü–∏—è: {text[:100]}...")
            # –°–ø–µ—Ü–∏–∞–ª—å–Ω—ã–π –º–∞—Ä–∫–µ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞
            return "INJECTION_ATTEMPT_DETECTED"
    
    return text.strip()
    
