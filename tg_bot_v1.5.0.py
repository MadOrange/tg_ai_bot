def convert_old_format(old_data: Dict) -> Dict[int, Dict[str, Any]]:
    """–ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç —Å—Ç–∞—Ä—ã–π —Ñ–æ—Ä–º–∞—Ç (—Ç–æ–ª—å–∫–æ —Å–ø–∏—Å–æ–∫ —Å–æ–æ–±—â–µ–Ω–∏–π) –≤ –Ω–æ–≤—ã–π."""
    new_data = {}
    current_time = datetime.now().isoformat()
    
    for user_id, messages in old_data.items():
        # –ï—Å–ª–∏ —ç—Ç–æ —É–∂–µ –Ω–æ–≤—ã–π —Ñ–æ—Ä–º–∞—Ç (—Å–ª–æ–≤–∞—Ä—å —Å –∫–ª—é—á–æ–º 'messages'), –æ—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å
        if isinstance(messages, dict) and 'messages' in messages:
            new_data[int(user_id)] = messages
        else:
            # –ï—Å–ª–∏ —Å—Ç–∞—Ä—ã–π —Ñ–æ—Ä–º–∞—Ç (–ø—Ä–æ—Å—Ç–æ —Å–ø–∏—Å–æ–∫ —Å–æ–æ–±—â–µ–Ω–∏–π) - –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º
            new_data[int(user_id)] = {
                'last_activity': current_time,
                'messages': messages
            }
    return new_data

def save_histories(histories: Dict[int, Dict[str, Any]]):
    """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç –∏—Å—Ç–æ—Ä–∏–∏ –¥–∏–∞–ª–æ–≥–æ–≤ –≤ —Ñ–∞–π–ª JSON —Å –∞–≤—Ç–æ–æ—á–∏—Å—Ç–∫–æ–π —Å—Ç–∞—Ä—ã—Ö –¥–∏–∞–ª–æ–≥–æ–≤."""
    # –û—á–∏—â–∞–µ–º –¥–∏–∞–ª–æ–≥–∏ —Å—Ç–∞—Ä—à–µ MAX_HISTORY_DAYS –¥–Ω–µ–π
    cleaned_histories = {}
    cutoff_date = datetime.now() - timedelta(days=MAX_HISTORY_DAYS)
    
    for user_id, user_data in histories.items():
        last_activity_str = user_data.get('last_activity', '')
        if last_activity_str:
            try:
                last_activity = datetime.fromisoformat(last_activity_str)
                if last_activity > cutoff_date:
                    cleaned_histories[user_id] = user_data
            except ValueError:
                # –ï—Å–ª–∏ –¥–∞—Ç–∞ –≤ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ, —Å–æ—Ö—Ä–∞–Ω—è–µ–º –¥–∏–∞–ª–æ–≥
                cleaned_histories[user_id] = user_data
        else:
            # –ï—Å–ª–∏ –Ω–µ—Ç –¥–∞—Ç—ã, —Å–æ—Ö—Ä–∞–Ω—è–µ–º (–º–æ–∂–µ—Ç –±—ã—Ç—å —Å—Ç–∞—Ä—ã–π —Ñ–æ—Ä–º–∞—Ç)
            cleaned_histories[user_id] = user_data
    
    try:
        with open(HISTORY_FILE, 'w', encoding='utf-8') as f:
            json.dump(cleaned_histories, f, ensure_ascii=False, indent=2)
    except IOError as e:
        logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞ –∏—Å—Ç–æ—Ä–∏–∏: {e}")

